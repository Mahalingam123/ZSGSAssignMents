package com.arithmetic;

 public   class Arithmetic{

   public int add(int x,int y)
{

  return x+y;
}


   public int sub(int x,int y)
{

  return x-y;
}


   public int mul(int x,int y)
{

  return x*y;
}


   public int divide(int x,int y)
{

  return x/y;
}

public int mod(int x,int y)
{

  return x%y;
}

}